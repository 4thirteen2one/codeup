#include<cstdio>
int main()
{
    int a[2][3],b[3][2];
    int c[4];
    int i,j;
    while(scanf("%d %d %d",&a[0][0],&a[0][1],&a[0][2]) != EOF){
        for(i = 1;i < 2;i++){
        for(j = 0;j < 3;j++){
            scanf("%d",&a[i][j]);
        }
    }
    for(i = 0;i < 3;i++){
        for(j = 0;j < 2;j++){
            scanf("%d",&b[i][j]);
        }
    }
    c[0] = a[0][0] * b[0][0] + a[0][1] * b[1][0] + a[0][2] * b[2][0];
    c[1] = a[0][0] * b[0][1] + a[0][1] * b[1][1] + a[0][2] * b[2][1];
    c[2] = a[1][0] * b[0][0] + a[1][1] * b[1][0] + a[1][2] * b[2][0];
    c[3] = a[1][0] * b[0][1] + a[1][1] * b[1][1] + a[1][2] * b[2][1];
    printf("%d %d\n",c[0],c[1]);
    printf("%d %d\n",c[2],c[3]);
    }
    return 0;
}
